Para probar las hamburguesas, los comensales deben visitar cada uno de estos lugares. Solo se necesitan las ganas por probarlas y su veredicto para escoger la mejor de Cúcuta. 
Es importante recordar que todas las hamburguesas estarán a 10 mil pesos y <strong>no aplican para llevar</strong>. </div>
